    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
 
        if(labelmain1.getText().equals("REGISTER")){
            if (!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", email.getText()))) 
           {
            JOptionPane.showMessageDialog(null, "Please enter a valid email", "Error", JOptionPane.ERROR_MESSAGE);
            }
          
            if(username.getText().length()==0||password.getText().length()==0||fname.getText().length()==0||
                    lname.getText().length()==0||
                email.getText().length()==0||phone.getText().length()==0)
            {

                JOptionPane.showMessageDialog(null,"All Details Compulsory!");
            }
            else if ((Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", email.getText())))
            {
                JOptionPane.showMessageDialog(null, "The email is valid", "Good!", JOptionPane.INFORMATION_MESSAGE);

            
                RegisterCall(username.getText(),password.getText(),fname.getText(),lname.getText(),email.getText(),
                        phone.getText());
            
            }
        }Visibility(true); 
    }//GEN-LAST:event_loginActionPerformed

