    void LoginCall(String usr, String pswd){
        //boolean flag=false;
        try{
           Class.forName("java.sql.Driver");
           Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/login","root","123456789");
           Statement stmt= conn.createStatement();
           String qrry = "select * from logindetails where username=\"" +usr+ "\" and password=\"" +pswd+ "\";";
           ResultSet rs= stmt.executeQuery(qrry);
           
            if(rs.next()){
               JOptionPane.showMessageDialog(null,"Sucessfully Logged In!");
                new Dashboard().setVisible(true);     
            }else{
                JOptionPane.showMessageDialog(null,"Invalid User! Register Now! ");
                new Login_form().setVisible(true);
            }
        }catch(HeadlessException | ClassNotFoundException | SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in connectivity"+ex);
        }
    }
     
    void RegisterCall(String usr,String pswd,String fname,String lname,String mail,String phone){
        try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/login","root","123456789");
           Statement stmt= conn.createStatement();
           String qrry = "Insert into logindetails values ("
                   + "'" +usr+ "',"
                   + "'" +pswd+ "',"
                   + "'" +fname+ "',"
                   + "'" +lname+ "',"
                   + "'" +mail+ "',"
                   + "'" +phone+ "'"
                   + ");";
           //JOptionPane.showMessageDialog(null,qrry);
           stmt.executeUpdate(qrry);
           stmt.close();
           conn.close();
           JOptionPane.showMessageDialog(null,"Registration successfull!");
           new Dashboard().setVisible(false); 
    }catch(HeadlessException | ClassNotFoundException | SQLException ex){
    
        JOptionPane.showMessageDialog(null,"Error in connectivity"+ex);
    }
    }
